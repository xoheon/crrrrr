from M import M

flag = open("./flag.txt", "r").read().encode()

n = 64
assert len(flag) == n and flag[:4] == b"ctf{" and flag[-1:] == b"}"
mod = 0x10001

result = []

assert len(M) == n
for v in M:
	assert len(v) == n

for i in range(n):
	dot = 0
	for j in range(n):
		dot += M[i][j] * flag[j]
	result.append(dot % mod)

print(result)
